package class1;

public class Hw1VariableValues {
    public static void main(String[] args) {
        /*
        Create a Java program and name it Variable Values
        In your program declare variables using different ways to store all different primitive data.
        Update value of all variables
        Print the value of each updated variable
         */

        byte temperature = 105;
        short biweeklyPay = 2000;
        int age = 17;
        long yearly = 120522L;
        float hourlyPay = 21.40F;
        double milliSec = 123456.789;
        boolean isHot = false;
        char screaming = '.';

        temperature = 104;
        biweeklyPay = 2100;
        age = 18;
        yearly = 121522L;
        hourlyPay = 21.50F;
        milliSec = 123456.790;
        isHot = true;
        screaming = '!';

        System.out.println(temperature);
        System.out.println(biweeklyPay);
        System.out.println(age);
        System.out.println(yearly);
        System.out.println(hourlyPay);
        System.out.println(milliSec);
        System.out.println(isHot);
        System.out.println(screaming);

    }
}
