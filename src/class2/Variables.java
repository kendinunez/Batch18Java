package class2;

public class Variables {
    public static void main(String[] args) {

        String fName = "Kendi";
        String lName = "Nunez";
        String city = "Suitland";
        String state = "Maryland";
        String phoneNum = "123-456-7890";

        System.out.println("My first name is "+fName+" and my last name is "+lName);
        System.out.println("I live in city "+city+" and state "+state);
        System.out.println("And my phone number is "+phoneNum);

        city = "Houston";
        state = "Texas";
        phoneNum = "101-102-1034";

        System.out.println("My first name is "+fName+" and I moved to a new city "+city+ " and new state "+state);
        System.out.println("My new phone number is "+phoneNum);
    }
}
