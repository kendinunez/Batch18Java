package review5;

public class E7Arrays {
    public static void main(String[] args) {
        int [] numbers={10, 25, 36, 56, 80};

        for (int number : numbers) {
            System.out.print(number+" ");
        }
        System.out.println();
        for (int i = 0; i < numbers.length; i++) {
            System.out.print(numbers[i]+" ");
        }

        }
    }

